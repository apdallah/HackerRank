// Complete the breakingRecords function below.
fun breakingRecords(scores: Array<Int>): Array<Int> {
   val result= arrayOf(0,0)
    var currentMin=scores[0]
    var currentMax=scores[0]
    scores.forEach { 
            if(it>currentMax){
                result[0]+=1
                currentMax=it
            }
            
            if(it<currentMin){
                result[1]++
                currentMin=it
            }
     }
     return result


}

fun main(args: Array<String>) {
